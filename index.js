// create board
function createBoard() {
  for (i = 0; i < layout.length; i++) {
    const square = document.createElement('div');
    switch(layout[i]) {
      case 0:
        square.classList.add('pac-dot');
        break;
      case 1:
        square.classList.add('wall');
        break;
      case 2:
        square.classList.add('ghost-lair');
        break;
      case 3:
        square.classList.add('power-pellet');
        break;
    }
    grid.appendChild(square);
    squares.push(square);
  }
  squares[locPacman].classList.add('pacman');
  // console.log(squares);
}

// get next position
function getNextPosition(e) {
  let to;
  let from = locPacman;
  switch(e.key) {
    case "ArrowUp":
      to = from - width;
      break;
    case "ArrowDown":
      to = from + width;
      break;
    case "ArrowLeft":
      if (from % width === 0) {
        from += width;
      }
      to = from - 1;
      break;
    case "ArrowRight":
        if ((from + 1) % width === 0) {
          from -= width;
        }
      to = from + 1;
      break;
  }

  if (to < 0 ||
      to > squares.length ||
      squares[to].classList.contains('wall') ||
      squares[to].classList.contains('ghost-lair')) {
    return locPacman;
  }

  return to;
}

// move to position
function moveTo(to) {
  squares[locPacman].classList.remove('pacman');
  squares[to].classList.add('pacman');
  locPacman = to;
  if (squares[to].classList.contains('pac-dot') ||
      squares[to].classList.contains('power-pellet')) {
    squares[to].classList.remove('pac-dot');
    squares[to].classList.remove('power-pellet');
    myScore += 1;
    scoreDisplay.innerHTML = myScore;
  }
}

// control pacman
function movePacman(e) {
  moveTo(getNextPosition(e));
}

createBoard();
document.addEventListener('keyup', movePacman);
